const ALL_TYPES: PokemonTypeName[] = [
  "normal", "fire", "water", "electric", "grass", "ice", "fighting", "poison",
  "ground", "flying", "psychic", "bug", "rock", "ghost", "dragon", "dark", "steel", "fairy",
];

export interface TypeMatchup {
  type: PokemonTypeName;
  multiplier: number;
}

/** Returns damage multipliers grouped by category for a defender with the given types. */
export function getDefensiveMatchups(defenderTypes: PokemonTypeName[]): TypeMatchup[] {
  return ALL_TYPES.map((attacker) => {
    const multiplier = defenderTypes.reduce((acc, def) => {
      const mod = ATTACK_CHART[attacker]?.[def];
      return acc * (mod ?? 1);
    }, 1);
    return { type: attacker, multiplier };
  });
}

/** Types that deal ≥2× damage to the defender, sorted by lethality descending. */
export function getWeaknesses(defenderTypes: PokemonTypeName[]): TypeMatchup[] {
  return getDefensiveMatchups(defenderTypes)
    .filter((m) => m.multiplier >= 2)
    .sort((a, b) => b.multiplier - a.multiplier);
}
